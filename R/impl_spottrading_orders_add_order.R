    return(place_order_helper(
        keys = keys,
        base_url = base_url,
        endpoint = "/api/v1/hf/orders",
        type = type,
        symbol = symbol,
        side = side,
        clientOid = clientOid,
        price = price,
        size = size,
        funds = funds,
        stp = stp,
